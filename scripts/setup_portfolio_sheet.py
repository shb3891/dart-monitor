    ws.update([headers], range_name='A1:W1')
    ws.format('A1:W1', {
        'textFormat': {'bold': True, 'foregroundColor': {'red': 1, 'green': 1, 'blue': 1}},
        'backgroundColor': {'red': 0.12, 'green': 0.3, 'blue': 0.47},
        'horizontalAlignment': 'CENTER',
        'verticalAlignment': 'MIDDLE',
    })
