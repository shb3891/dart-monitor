sh = gc.open_by_key(SHEET_ID)


def main():
    today = datetime.now().strftime('%Y-%m-%d')
    print(f"🚀 풋콜스케줄 시트 생성 시작 ({today})\n")
    
    sheet_name = '풋콜스케줄'
    
    # 1. 시트 생성 또는 초기화
    try:
        ws = sh.worksheet(sheet_name)
        print(f"  ℹ '{sheet_name}' 시트 이미 존재 — 데이터 모두 삭제 후 새로 작성")
        ws.clear()
    except Exception:
        # 시트 크기는 데이터 + 여유분
        rows_needed = len(SCHEDULE_DATA) + 200
        ws = sh.add_worksheet(title=sheet_name, rows=rows_needed, cols=10)
        print(f"  ✅ '{sheet_name}' 시트 새로 생성 ({rows_needed} rows)")
    
    # 2. 헤더 (9개 컬럼: A~I)
    headers = [
        '종목코드',       # A
        '종목명',         # B
        '이벤트유형',     # C - 드롭다운
        '차수',           # D
        '시작일',         # E - 봇 알림 기준
        '종료일',         # F
        '지급일',         # G
        '비율/금리',      # H
        '비고',           # I
    ]
    ws.update([headers], range_name='A1:I1')
    ws.format('A1:I1', {
        'textFormat': {'bold': True, 'foregroundColor': {'red': 1, 'green': 1, 'blue': 1}},
        'backgroundColor': {'red': 0.12, 'green': 0.3, 'blue': 0.47},
        'horizontalAlignment': 'CENTER',
        'verticalAlignment': 'MIDDLE',
    })
    ws.freeze(rows=1)
    print(f"  ✅ 헤더 설정 완료")
    
    # 3. 데이터 입력 (789행)
    print(f"\n📝 데이터 입력 중 ({len(SCHEDULE_DATA)}건)...")
    
    rows = []
    for evt in SCHEDULE_DATA:
        rows.append(list(evt))
    
    # 한 번에 업데이트
    end_row = len(rows) + 1
    
    # 큰 데이터는 청크로 나눠서
    BATCH_SIZE = 500
    for i in range(0, len(rows), BATCH_SIZE):
        chunk = rows[i:i + BATCH_SIZE]
        start = i + 2  # 1행은 헤더
        end = start + len(chunk) - 1
        ws.update(chunk, range_name=f'A{start}:I{end}')
        print(f"  ✅ {min(i + BATCH_SIZE, len(rows))}/{len(rows)}개")
    
    # 4. 통계
    from collections import Counter
    evt_counter = Counter(evt[2] for evt in SCHEDULE_DATA)
    
    print(f"\n🏁 완료!")
    print(f"  📊 총 입력: {len(SCHEDULE_DATA)}행")
    for evt_type, cnt in sorted(evt_counter.items()):
        print(f"     - {evt_type}: {cnt}건")
    print(f"\n  👉 https://docs.google.com/spreadsheets/d/{SHEET_ID}")
    print(f"\n📌 다음 단계:")
    print(f"  - PUT/CALL은 시트에서 수기 입력")
    print(f"  - 종목별로 정렬되어 있으니 같은 종목 행 사이에 PUT 행 추가")


if __name__ == '__main__':
    main()
